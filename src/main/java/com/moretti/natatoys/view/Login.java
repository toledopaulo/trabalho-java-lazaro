    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed

        String email = txtEmail.getText().trim();
        String senha = new String(txtSenha.getPassword()); // Converte o JPasswordField para String

        String[] resultado = loginService.validarLogin(email, senha); // Valida o login

        if (resultado != null) { // Certifica que o array retornado tem 2 elementos
            String tipo = resultado[0]; // Primeiro elemento: tipo (admin ou cliente)
            System.out.println(tipo);
            if ("admin".equals(tipo)) {
                new PrincipalAdmin().setVisible(true); // Redireciona para tela de admin
            } else if ("cliente".equals(tipo)) {
                new PrincipalCliente().setVisible(true); // Redireciona para tela de cliente
            }
                this.dispose();
        } else {
                JOptionPane.showMessageDialog(this, "UsuÃ¡rio ou senha incorretos!");
        }       // TODO add your handling code here:
    }//GEN-LAST:event_loginBtnActionPerformed

